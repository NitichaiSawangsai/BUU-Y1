using namespace std;
#include <iostream>
int main() {
		cout << "Start example 5" << endl;
	int a; 						// Line 10
	double z; 					// Line 11
	char ch; 					// Line 12
		cout << "Enter three values "; 		// Line 13
		cin  >> a >> ch >> z; 			// Line 14
		cout << "Line 15: a = " << a
		      << " , ch = " << ch
	 	      << " , z = " << z << endl;
	return 0;
}//main
